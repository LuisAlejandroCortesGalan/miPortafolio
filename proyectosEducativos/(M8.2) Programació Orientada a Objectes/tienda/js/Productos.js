class Productos {

    constructor(id, nombre, precio, stock) {
        this.id = id;
        this.nombre = nombre;
        this.precio = precio;
        this.stock = stock;
    }

    toString() {
        return `<br>Nombre del producto: ${this.nombre},<br>Id: ${this.id},<br>Precio: ${this.precio},<br>Stock: ${this.stock}`;
    }
}

